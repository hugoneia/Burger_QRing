import React from 'react';
import { StyleSheet, View } from 'react-native';
import QRCode from 'react-native-qrcode-svg';
import { useColors } from '@/hooks/useColors';
import { Feather } from '@expo/vector-icons';

const QR_SIZE = 210;

export function QRCodeCard({ value }: { value: string }) {
  const colors = useColors();

  // Esta era la transformación exacta de ayer a mediodía:
  // Se eliminaban espacios y se aseguraba el string plano nativo sin saltos extraños
  const cleanValue = value ? value.trim() : '';

  return (
    <View
      style={[
        styles.card,
        { backgroundColor: colors.card, borderColor: colors.border, borderRadius: colors.radius },
      ]}
    >
      {cleanValue ? (
        <View style={styles.qrWrap}>
          <QRCode 
            value={cleanValue} 
            size={QR_SIZE} 
            color="#0B0F0E" 
            backgroundColor="#FFFFFF"
            // Ayer NO pasábamos el parámetro 'ecl', iba completamente vacío
            // permitiendo que la librería auto-detectara el formato nativo del TPV
          />
        </View>
      ) : (
        <View style={[styles.placeholder, { width: QR_SIZE, height: QR_SIZE }]}>
          <Feather name="square" size={40} color={colors.mutedForeground} />
        </View>
      )}
    </View>
  );
}

const styles = StyleSheet.create({
  card: {
    borderWidth: 1,
    padding: 20,
    alignItems: 'center',
    justifyContent: 'center',
  },
  qrWrap: {
    backgroundColor: '#FFFFFF',
    padding: 14,
    borderRadius: 12,
  },
  placeholder: {
    alignItems: 'center',
    justifyContent: 'center',
    borderRadius: 12,
    borderWidth: 1,
    borderColor: 'rgba(255,255,255,0.08)',
  },
});
